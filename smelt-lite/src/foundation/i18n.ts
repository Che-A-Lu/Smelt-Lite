let lang: "zh" | "en" = "zh";
try {
  const saved = localStorage.getItem('smelt-lite-lang');
  if (saved === 'zh' || saved === 'en') {
    lang = saved;
  } else {
    lang = navigator.language.startsWith("zh") ? "zh" : "en";
  }
} catch { /* SSR guard */ }

export function t(key: string, replacements?: Record<string, string | number>): string {
  const map = lang === "zh" ? zh : en;
  let val = map[key] ?? key;
  if (replacements) {
    for (const [k, v] of Object.entries(replacements)) {
      val = val.replace(`{${k}}`, String(v));
    }
  }
  return val;
}

export function getLang(): "zh" | "en" { return lang; }

export function setLang(l: "zh" | "en"): void {
  lang = l;
  try { localStorage.setItem('smelt-lite-lang', l); } catch { /* ignore */ }
}
